package javabean;

/**
 *
 * @author apple
 */
public class StylesBean {
    
    private String topic_state;
    private String word_count;
    private String view_style;

    public StylesBean() {
    }

    public String getTopic_state() {
        return topic_state;
    }

    public void setTopic_state(String topic_state) {
        this.topic_state = topic_state;
    }

    public String getWord_count() {
        return word_count;
    }

    public void setWord_count(String word_count) {
        this.word_count = word_count;
    }

    public String getView_style() {
        return view_style;
    }

    public void setView_style(String view_style) {
        this.view_style = view_style;
    }
    
    
    
}
